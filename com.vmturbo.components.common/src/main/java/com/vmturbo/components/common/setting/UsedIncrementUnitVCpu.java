package com.vmturbo.components.common.setting;

/**
 * {@link UsedIncrementUnitVCpu} contains declarations of units supported by used increment settings for VCPU.
 */
public enum UsedIncrementUnitVCpu {
  /**
   * Megahertz units for VCPU used increment values.
   */
  MHZ,
  /**
   * Sockets amount will be used for VCPU used increment values.
   */
  SOCKETS;
}
